#include <libc.h>

void	ft_putchar(char c)
{
	char	b1;
	char	b2;

	if (c >= 0)
		write(1, &c, 1);
	else
	{
		b1 = 195 & (c >> 6);
		b2 = 191 & c;
		write(1, &b1, 1);
		write(1, &b2, 1);
	}
}
