int		ft_isalpha(int c)
{
	unsigned char	c_char;

	if (c > 255 || c < 0)
		return (0);
	c_char = (unsigned char)c;
	if (((c_char >= '\141') && (c_char <= '\172')) ||
			((c_char >= '\101') && (c_char <= '\132')))
		return (1);
	return (0);
}
